def get_persona_icon(persona: str) -> str:
    p = persona.lower()
    for key, icon in PERSONA_ICON.items():
        if key in p:
            return icon
    return "👤"


def select_framework(customer: dict) -> str:
    """Return the framework key for this customer. Priority: persona → cluster → default."""
    persona = str(customer.get("persona", "")).lower()

    if "tự thưởng" in persona:
        return "Hero's Journey"
    if "cầu hôn" in persona:
        return "Golden Circle"
    if "kỷ niệm" in persona:
        return "Golden Circle"
    if "quà" in persona:
        return "Emotional Branding"

    cluster = str(customer.get("cluster", "")).lower()
    if "self" in cluster or "reward" in cluster:
        return "Hero's Journey"
    if "engagement" in cluster:
        return "Golden Circle"
    if "anniversary" in cluster:
        return "Golden Circle"
    if "gift" in cluster:
        return "Emotional Branding"

    return "Hero's Journey"


def extract_signals(customer: dict) -> list[str]:
    """Return a list of human-readable active behavioral signals."""
    signals = []

    try:
        if int(customer.get("sig_search_propose", 0)) == 1:
            signals.append("🔍 Tìm kiếm trang sức cầu hôn")
    except (ValueError, TypeError):
        pass

    try:
        val = float(customer.get("sig_view_engagement_ring", 0))
        if val > 1:
            signals.append(f"💍 Đã xem nhẫn đính hôn {int(val)} lần")
    except (ValueError, TypeError):
        pass

    try:
        val = float(customer.get("sig_view_diamond", 0))
        if val > 1:
            signals.append("💎 Quan tâm đặc biệt đến kim cương")
    except (ValueError, TypeError):
        pass

    try:
        val = float(customer.get("sig_birthday_in_days", 999))
        if val < 60:
            signals.append(f"🎂 Có dịp kỷ niệm sau {int(val)} ngày")
    except (ValueError, TypeError):
        pass

    try:
        if float(customer.get("camp_selfreward", 0)) > 0:
            signals.append("🏆 Lịch sử mua dịp tự thưởng")
    except (ValueError, TypeError):
        pass

    try:
        if float(customer.get("camp_anniversary", 0)) > 1:
            signals.append("🎊 Thường mua quà dịp kỷ niệm")
    except (ValueError, TypeError):
        pass

    try:
        if float(customer.get("monetary", 0)) > 50_000_000:
            signals.append("⭐ Khách VIP — chi tiêu cao")
    except (ValueError, TypeError):
        pass

    try:
        if float(customer.get("frequency", 0)) >= 3:
            signals.append("🔄 Khách hàng trung thành")
    except (ValueError, TypeError):
        pass

    return signals


